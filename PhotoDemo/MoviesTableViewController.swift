        let fileManager = FileManager.default
        let docUrls =
            fileManager.urls(for: .documentDirectory, in: .userDomainMask)
        let docUrl = docUrls.first
        let url = docUrl?.appendingPathComponent("movies.txt")
        let array = NSArray(contentsOf: url!)
        if array != nil {
            movies = array as! [[String:String]]
        }
        
        let notiName = Notification.Name("AddMovie")
        NotificationCenter.default.addObserver(self, selector: #selector(MoviesTableViewController.getAddMovieNoti(noti:)), name: notiName, object: nil)
    }

